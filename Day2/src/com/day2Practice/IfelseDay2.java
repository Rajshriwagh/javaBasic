package com.day2Practice;

import java.util.Scanner;

public class IfelseDay2 {
	public static void main(String[] args) {
		
		int x;
		System.out.print("Enter an Value :: ");
		Scanner sc = new Scanner(System.in);
		x = sc.nextInt();
		
		if (x == 0) {
			System.out.print("Value of X is 0");

		} else if (x == 1) {
			System.out.print("Value of X is 1");

		} else if (x == 2) {
			System.out.print("Value of X is 2");

		} else if (x == 3) {
			System.out.print("Value of X is 3");

		} else if (x == 4) {
			System.out.print("Value of X is 4");

		} else if (x == 5) {
			System.out.print("Value of X is 5");

		} else if (x == 6) {
			System.out.print("Value of X is 6");

		} else if (x == 7) {
			System.out.print("Value of X is 7");

		} else if (x == 8) {
			System.out.print("Value of X is 8");

		} else if (x == 9) {
			System.out.print("Value of X is 9");

		} else {
			System.out.print("This is not a single digit value");
		}

	}
}